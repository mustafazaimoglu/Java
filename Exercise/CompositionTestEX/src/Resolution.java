public class Resolution 
{
    private String En;
    private String Boy;

    public Resolution(String En, String Boy) {
        this.En = En;
        this.Boy = Boy;
    }

    public String getEn() {
        return En;
    }

    public void setEn(String En) {
        this.En = En;
    }

    public String getBoy() {
        return Boy;
    }

    public void setBoy(String Boy) {
        this.Boy = Boy;
    }
    
    
    
}
