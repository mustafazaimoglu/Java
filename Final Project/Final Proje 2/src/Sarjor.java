
public class Sarjor
{
    private int kapasite;
    private int KalanMermi;

    public Sarjor(int kapasite, int KalanMermi)
    {
        this.kapasite = kapasite;
        this.KalanMermi = KalanMermi;
    }

    public int getKapasite()
    {
        return kapasite;
    }

    public void setKapasite(int kapasite)
    {
        this.kapasite = kapasite;
    }

    public int getKalanMermi()
    {
        return KalanMermi;
    }

    public void setKalanMermi(int KalanMermi)
    {
        this.KalanMermi = KalanMermi;
    }
    
    
    
}
